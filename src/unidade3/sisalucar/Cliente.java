package unidade3.sisalucar;

public class Cliente {

	long idCliente;
	String cpf;
	String cnh;
	String nome;

	public Cliente() {

	}

	public Cliente(long idCliente, String cpf, String cnh, String nome) {
		super();
		this.idCliente = idCliente;
		this.cpf = cpf;
		this.cnh = cnh;
		this.nome = nome;
	}

}
